.section .text
.extern interrupt_handler    

per_vector_entry:
    push %eax
    push %ecx
    push %edx
    push %ds  # to now, the ESP contains the address of the interrup_argument
    push %esp # push it as arguments
    call interrupt_handler
    pop %eax  #pop the argument
    pop %ds
    pop %edx
    pop %ecx
    pop %eax
    add $8, %esp # skip interrup vector number and errorcode
    iret

.macro DEF_INT_ENTRY_WITH_ERRORCODE vectoor
.global int\vectoor
int\vectoor:
    cli
    push $\vectoor
    jmp per_vector_entry
.endm

.macro DEF_INT_ENTRY_WITHOUT_ERRORCODE vectoor
.global int\vectoor
int\vectoor:
    cli
    push $0
    push $\vectoor
    jmp per_vector_entry
.endm

